export const getBackgroundSportByID = sportID => SportBetroute[sportID].background ?
  SportBetroute[sportID].background : football;

export function getSportImgByID(sportID) {
  const sportIcon = SportBetroute[sportID] ? SportBetroute[sportID].pngIcon : defaultIcon;
  return sportIcon;
}

export function getSportColorByID(sportID) {
  const color = SportBetroute[sportID] && SportBetroute[sportID].color ? SportBetroute[sportID].color : 'default';
  return color;
}

export function getSportByPriorityID(sportID) {
  const priority = (SportBetroute[sportID] && SportBetroute[sportID].priority) ? SportBetroute[sportID].priority : 100;
  return priority;
}

export function sortSportByPriority(sports) {
  return sports.sort((a, b) => getSportByPriorityID(a.ID) - getSportByPriorityID(b.ID));
}
